export function getElevationAtIndex(index: number): number {
  if (index < 0 || index >= detailedRouteCoordinates.length) return 0;
  return detailedRouteCoordinates[index][2];
}

// Get day info at a specific index
export function getDayAtIndex(index: number): { day: number; name: string } {
  let currentDay = dayBoundaries[0];
  for (const boundary of dayBoundaries) {
    if (index >= boundary.index) {
      currentDay = boundary;
    }
  }
  return { day: currentDay.day, name: currentDay.name };
}

